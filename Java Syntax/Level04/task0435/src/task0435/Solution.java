package task0435;

/* 
Even numbers (from the range from 1 to 100)
*/

public class Solution {
	
    public static void main(String[] args) throws Exception {
    
    	for (int i = 1; i <= 100; i++) {
    		if (i % 2 == 0) 
    			
    		{
    			System.out.println(i);
    		}
    	}
    }
}

    
    
    
